// Given the Day number, print the Day name in lower case corresponding to it by using the help of switch statement.

// Note: Day 1 - is monday.

// If the day is not valid example 1> day >7 then print invalid



// Input :-

// Day - 3

// Output :-

// wednesday

// https://www.youtube.com/watch?v=YsApVO7SDxc
let day='monday'
switch(day){
    case'monday':
        console.log('1')
        break
        case 'tuesday':
            console.log('2')
            break
            case 'wednesday':
                console.log('3')
                break
                case 'thursday':
                    console.log('4')
                    break
                    case 'friday':
                        console.log('5')
                        break
                        case 'saturday':
                            console.log('6')
                            break
                            case'sunday':
                                console.log('7')
                                break
}